CREATE TABLE USUARIO(
	IDUSUARIO BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NOME VARCHAR(50) NOT NULL,
	SOBRENOME VARCHAR(100) NOT NULL,
	EMAIL VARCHAR(100) UNIQUE NOT NULL,
	DATA_CADASTRO TIMESTAMP NOT NULL,
	DISPONIBILIDADE VARCHAR(21) NOT NULL,
	LINGUAGEM VARCHAR(30) NOT NULL,
	AREA VARCHAR(50) NOT NULL,
	DESCRICAO VARCHAR(300) NOT NULL,
	STATUS VARCHAR(14) CHECK (STATUS IN ('FILA_DE_ESPERA', 'APROVADO', 'REPROVADO','CANCELADO','CONCLUIDO')) NOT NULL,

	CONSTRAINT PK_USUARIO PRIMARY KEY(IDUSUARIO)
);